class character():
	"""docstring for character"""


	def __init__(self, matrix):
		
		global font
		global backGround
		self.matrix = matrix
		self.height = len(matrix)
		self.width = len(matrix[0])

		
	def printMatrix(self):
		print("[")
		for x in range(0,self.height):
			string = "\t[ "
			for y in range(0, self.width-1):
				string = string + str(self.matrix[x][y]) + ", "
			string = string + str(self.matrix[x][self.width-1]) + " ],"
			print(string)
		print("]")

	def printGrid(self):
		global font, backGround
		for x in range(0, self.height):
			string = ""
			for y in range(0, self.width):
				if (self.matrix[x][y]):
					c = 'XX'
				else :
					c = '--'
				string = string + c
			print(string)
		print("")

	def self(self):
		return self.matrix




